    public ArrayList<String> getProvinceList() {
        ArrayList<AddrList> addrLists = getAddrList();
        ArrayList<String> provinceList = new ArrayList<>();

        for (AddrList s : addrLists) {
            if (!provinceList.contains(s.province)) {
                provinceList.add(s.province);
            }
        }
        return provinceList;
    }

    public ArrayList<String> getDistrictList(String province) {
        ArrayList<AddrList> addrLists = getAddrList();
        ArrayList<String> districtList = new ArrayList<>();

        for (AddrList s : addrLists) {
            if (s.province.equals(province)) {
                districtList.add(s.district);
            }
        }
        return districtList;
    }

    public ArrayList<String> getPostcodeList(String district) {
        ArrayList<AddrList> addrLists = getAddrList();
        ArrayList<String> postcodeList = new ArrayList<>();

        for (AddrList s : addrLists) {
            if (s.district.equals(district)) {
                postcodeList.add("" + s.postcode);
            }
        }
        return postcodeList;
    }


